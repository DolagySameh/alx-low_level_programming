#include <stdio.h>
#include "3-calc.h"
/**
* op_add - sum of a and b
* Description:sum of a and b
* @a:first arg
* @b:second arg
* Return:int
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub - sub of a and b
* Description:sub of a and b
* @a:first arg
* @b:second arg
* Return:int
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - mul of a and b
* Description:mul of a and b
* @a:first arg
* @b:second arg
* Return:int
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - div of a and b
* Description:div of a and b
* @a:first arg
* @b:second arg
* Return:int
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mod - mod of a and b
* Description:mod of a and b
* @a:first arg
* @b:second arg
* Return:int
*/
int op_mod(int a, int b)
{
	return (a % b);
}
